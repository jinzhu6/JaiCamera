	/// \param [in] strPath		저장 경로 입력.
	/// \param bool				결과 반환.
	bool OnSaveImage(CString strPath);
	//******************************************************************************************************************
	/// \brief					User Set 저장 함수.
	/// \param bool				결과 반환.
	bool OnUserSetSave();
	//******************************************************************************************************************
	/// \brief					User Set 불러오기 함수.
	/// \param bool				결과 반환.
	bool OnUserSetLoad();
	//******************************************************************************************************************
	/// \brief					Auto White Balance 설정 함수.
	/// \param bool				결과 반환.
	bool OnCalculateWhiteBalance();

	//----- 확인 및 반환 함수 -----//
