	/// \brief					카메라를 해제하는 함수.
	/// \param bool				결과 반환.
	bool OnDisconnect();
	//******************************************************************************************************************
	/// \brief					영상 취득 시작 함수.
	/// \param bool				결과 반환.
	bool OnStartAcquisition();
